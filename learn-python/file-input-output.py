# FILE HANDLING

# The key function for working with files in python is the open()function.function
# The open()function takes two parameters: filename and mode.
# There are four different methods (modes) for opening a file.

# "r" Read default value opens a file for reading, error if the file does not exist
# "a" Append - Opens a file for appending, creates the file if it does not exist
# "w" Write - Open a file for writing, creates the file if it doest not exist
# "x" Create - Creates the specified file, returns an error if the file exist 
# In addition you can specify if the should handle as binary or text mode 
# "t" Text-Default value Text mode
# "b" Binry-Binary mode(e.g images)







